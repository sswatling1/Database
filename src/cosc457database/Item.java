package cosc457database;

/**
 *
 * @author Scott
 */
class Item {
    int idItem;
    String name;
    float price;

    public Item(int idItem, String name, float price) {
        this.idItem = idItem;
        this.name = name;
        this.price = price;
    }

    public int getIdItem() {
        return idItem;
    }

    public void setIdItem(int idItem) {
        this.idItem = idItem;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    
    
    
}
