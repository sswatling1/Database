package cosc457database;

/**
 *
 * @author Scott
 */
class Inventory {
    int idItem;
    String name;
    float price;
    int qty;

    public Inventory(int idItem, String name, float price, int qty) {
        this.idItem = idItem;
        this.name = name;
        this.price = price;
        this.qty = qty;
    }

    public int getIdItem() {
        return idItem;
    }

    public void setIdItem(int idItem) {
        this.idItem = idItem;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }
    
    
}
